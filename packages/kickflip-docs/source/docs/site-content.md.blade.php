---
title: Creating Your Site's Content
---

All the content on your Kickflip site should be stored in the `source` directory.
Creating content is very flexible with multiple rendering choices for source files.
Supported extensions include:
- [Markdown]({{ getDocUrl('site-content-markdown') }}),
- [Laravel's Blade template language]({{ getDocUrl('site-content-templates-and-components') }}), or
- a [mixture of other types]({{ getDocUrl('site-content-other-file-types') }}).
